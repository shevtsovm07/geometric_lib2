
def area(a):

    '''
        Вычисляет площадь квадрата.

        Параметры:
            a (число): длина стороны квадрата

        Возвращает:
            число: площадь квадрата

    '''
    return a * a


def perimeter(a):

    '''
        Вычисляет периметр квадрата.

        Параметры:
            a (число): длина стороны квадрата

        Возвращает:
            число: периметр квадрата

    '''
    return 4 * a
