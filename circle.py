import math



def area(r):
    '''
    Вычисляет площадь круга.

    Параметры:
        r (число): радиус круга

    Возвращает:
        число: площадь круга

    '''
    return math.pi * r * r


def perimeter(r):
    '''
        Вычисляет длину окружности.

        Параметры:
            r (число): радиус круга

        Возвращает:
            число: длина окружности

        '''
    return 2 * math.pi * r

